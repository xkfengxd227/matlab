%% compare with CPU response time between HB and HB+
% some common config
linesolid = ['-ks'; '-ro'; '-g^'; '-cv'; '-md'; '-ko';'-rv'];
linedashed = [':ks'; ':ro'; ':g^'; ':cv'; ':md'; ':ko';':rv'];
hold on;
box on;
set(gcf,'Position',[100 100 1400, 720]);

%% CPU response time
% Corel
subplot(2,3,1);
x_nk = [40:40:200];
y_cpu_hb = [850,900,1148,1605,2186] / 1000;
y = [...
    713,685,808,1113,1454;...
    620,658,841,1197,1632;...
    601,666,910,1321,1829;...
    598,691,977,1441,1970;...
    600,707,1015,1486,2127] / 1000;
hold on;
box on;
plot(x_nk, y_cpu_hb, linesolid(6,:), ...
        'LineWidth',1.5, 'MarkerSize',7, 'MarkerFaceColor', 'g');
plot(...
    x_nk, y(1,:), linesolid(1,:),...
    x_nk, y(2,:), linesolid(2,:),...
    x_nk, y(3,:), linesolid(3,:),...
    x_nk, y(4,:), linesolid(4,:),...
    x_nk, y(4,:), linesolid(5,:),...
    'LineWidth',1.5, 'MarkerSize',7);
title('Corel', 'Fontname', 'Helvetica', 'FontAngle', 'Normal', 'FontWeight', 'Normal');
xlabel('K');
ylabel('CPU response time(ms)');
h=legend('HB', 'HB+ alpha=0.02', 'HB+ alpha=0.04', 'HB+ alpha=0.06', 'HB+ alpha=0.08', 'HB+ alpha=0.1');
set(h, 'EdgeColor', 'White', 'Location', 'NorthWest');
axis([min(x_nk), max(x_nk), 0.5, 3]);
set(get(gca,'XLabel'),'FontSize',9, 'FontAngle', 'Italic', 'FontWeight', 'Normal', 'Vertical','top');
set(get(gca,'YLabel'),'FontSize',10, 'FontAngle', 'Italic', 'FontWeight', 'Normal', 'Vertical','bottom');
set(gca, 'Fontname', 'Helvetica',  'FontSize',10);
set(gca, 'XTick', x_nk');
set(gca, 'YTick', [0.5:0.5:3]);


        x_nk, y_io(1,:), linedashed(1,:), ...
        x_nk, y_io(2,:), linedashed(2,:), ...
        x_nk, y_io(3,:), linedashed(3,:), ...
        x_nk, y_io(4,:), linesolid(4,:), ...
        x_nk, y_io(5,:), linedashed(5,:), ...
        'LineWidth',2, 'MarkerSize',9);


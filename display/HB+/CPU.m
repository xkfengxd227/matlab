%% compare  CPU response time between HB, HBPlus, VA-File, iDistance
% common configurations
linesolid = ['-ks'; '-ro'; '-g^'; '-cv'; '-md'; '-ko';'-rv';'-b+'];
linedashed = [':ks'; ':ro'; ':g^'; ':cv'; ':md'; ':ko';':rv';':b+'];
hold on;
box on;
set(gcf,'Position',[100 100 1400, 720]);
x_k = [10:30:100];

% Corel
subplot(2,3,1);
y_cpu = [...
    44561,44006,44113,44835;...
    2000,3000,4000,6000;...
    10454,14893,18034,20724;...
    1148,1335,1487,1559;...
    977,1147,1239,1290] / 1000;
semilogy(...
        x_k, y_cpu(1,:), linesolid(1,:), ...
        x_k, y_cpu(2,:), linesolid(2,:), ...
        x_k, y_cpu(3,:), linesolid(8,:), ...
        x_k, y_cpu(4,:), linesolid(3,:), ...
        x_k, y_cpu(5,:), linesolid(5,:), ...
        'LineWidth',1.5, 'MarkerSize',7);
title('Corel', 'Fontname', 'Helvetica', 'FontAngle', 'Normal', 'FontWeight', 'Normal');
xlabel('k');
ylabel('CPU response time(ms)');
h=legend('VA-File', 'iDistance', 'FNN', 'HB', 'HB+');
set(h, 'EdgeColor', 'White', 'Location', 'West');
axis([min(x_k), max(x_k), 0, 80]);
set(get(gca,'XLabel'),'FontSize',9, 'FontAngle', 'Italic', 'FontWeight', 'Normal', 'Vertical','top');
set(get(gca,'YLabel'),'FontSize',10, 'FontAngle', 'Italic', 'FontWeight', 'Normal', 'Vertical','bottom');
set(gca, 'Fontname', 'Helvetica',  'FontSize',9);
set(gca, 'XTick', x_k');
set(gca, 'YTick', [0:20:80]);

